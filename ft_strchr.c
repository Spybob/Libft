#include "libft.h"

char	*ft_strchr(const char *s1, int c)
{
	char	*u1;
	int		i;

	u1 = (char *)s1;
	i = 0;
	while (u1[i])
	{
		if (u1[i] == c)
			return (u1 + i);
		i++;
	}
	if (u1[i] == '\0' && c == '\0')
		return (u1 + i);
	return (NULL);
}
