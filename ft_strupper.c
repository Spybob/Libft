#include "libft.h"

char	*ft_strupper(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (97 <= str[i] && str[i] <= 122)
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}
